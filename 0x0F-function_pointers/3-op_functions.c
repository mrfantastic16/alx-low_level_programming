/**
 * op_add - calculates the sum of the two numbers received
 * @a: num1
 * @b: num2
 *
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculates the difference the two nums
 * @a: num1
 * @b: num2
 *
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calculates the product of two nums
 * @a: num1
 * @b: num2
 *
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calculates the division of one num with another
 * @a: quotient
 * @b: divisor
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns num mod num
 * @a: num1
 * @b: num2
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}



















